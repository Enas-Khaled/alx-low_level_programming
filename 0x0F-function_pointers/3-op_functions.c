#include "3-calc.h"
/**
 * op_add - (int a, int b)
 * @a: num1
 * @b: num2
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - (int a, int b)
 * @a: num1
 * @b: num2
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - (int a, int b)
 * @a: num1
 * @b: num2
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - (int a, int b)
 * @a: num1
 * @b: num2
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - (int a, int b)
 * @a: num1
 * @b: num2
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
